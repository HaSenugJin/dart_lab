void main(List<String> args) {
  // 같은가?
  print("3 == 2 = ${3 == 2}");

  // 다른가?
  print("3 != 2 = ${3 != 2}");

  // 왼쪽 값보다 큰가?
  print("3 < 2 = ${3 < 2}");

  // 왼쪽 값보다 작은가?
  print("3 > 2 = ${3 > 2}");

  // 왼쪽 값보다 크거나 같은가?
  print("3 <= 2 = ${3 <= 2}");

  // 왼쪽 값보다 작거나 같은가?
  print("3 >= 2 = ${3 >= 2}");
}
